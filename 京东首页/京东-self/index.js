function createNav(menuList){
    var cateAll = $('<div class = "cateAll"></div>');
    var subsAll = $('<div class = "subsAll"></div>');
    cateAll.appendTo($('#menu-content'));
    subsAll.appendTo($('#menu-content'));

    menuList.forEach(function(ele,index){
        var content = ele.content;
        var oli = $('<li></li>');
        ele.title.forEach(function(titles){
            var oa = $('<a href = "#">'+ titles +'</a>');
            var titlesNum = ele.title.length;
            oa.appendTo(oli);
            if(titlesNum > 1 && titles != ele.title[titlesNum -1]){
                oli.append($('<span>/</span>'));
            }
     })
        oli.appendTo($('#menu-list'));
        var cate_channel = $('<div class = "cate_channel"></div>');
        var subs = $('<div class = "subs"></div>');

        content.tabs.forEach(function(ele1,index){
            var ocontent = $('<a href = "#">'+ ele1 +'<i>&gt</i></a>');
            ocontent.appendTo($(cate_channel));
           
        }) 
        cate_channel.css({display:'none'}).appendTo($('.cateAll'));

        content.subs.forEach(function(ele2,index){
            var odl = $('<dl class = "cata_detail"></dl>');
            var osubs = $('<dt><a href = "#">'+ ele2.title +'</a></dt>');
            var odd = $('<dd></dd>');
            ele2.items.forEach(function(ele2,index){
                var oitems = $('<a href = "#">'+ ele2 +'</a>');
                oitems.appendTo($(odd));

            }) 
            odd.appendTo(odl);
            osubs.appendTo(odl);
            odl.appendTo(subs);
            subs.css({display:'none'}).appendTo('.subsAll');
        })
    })

    
}

